def get_country_from_mcc(mcc: str) -> str:
    """Get country name with flag from MCC code."""
    if not mcc:
        return ""
    
    # Normalize MCC (remove leading zeros, convert to string)
    mcc_str = str(int(mcc)) if mcc.isdigit() else str(mcc)
    
    # Pad to 3 digits
    mcc_str = mcc_str.zfill(3)
    
    return MCC_TO_COUNTRY.get(mcc_str, f"ðŸŒ MCC {mcc_str}")


def get_location_info(mcc: str, mnc: str = None) -> str:
    """Get location info from MCC/MNC codes."""
    if not mcc:
        return ""
    
    country = get_country_from_mcc(mcc)
    
    if mnc:
        return f"{country} (MNC {mnc})"
    else:
        return country
